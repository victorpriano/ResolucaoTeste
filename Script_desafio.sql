CREATE TABLE PRODUTO ( 
  ID INTEGER PRIMARY KEY AUTOINCREMENT,
  FK_COD_BARRA INTEGER,
  COD_INTERNO INTEGER, 
  PRECO DECIMAL(10,2),
  ESTOQUE INTEGER,
FOREIGN KEY (FK_COD_BARRA) REFERENCES ITEM(COD_BARRA)
);
  
CREATE TABLE ITEM (
  COD_BARRA INTEGER PRIMARY KEY NOT NULL,
  DESCRICAO VARCHAR(50));
  
